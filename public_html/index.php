                <h4> Qu'est ce que le <span class='rouge'> BTS SIO </span> ?</h4>
                <p> Le BTS SIO ( Services Informatiques aux Organisations ), est une formation d'études supérieures dure 2 ans. Les obtenteurs de ce BTS ont diplôme de niveau BAC+2 reconnu par l'Etat Français. <br>
                    Les étudiants en  BTS SIO sont, généralement, des lauréats d'un bac général, bac STMG ou bac pro du domaine. <br>
                    Deux options sont offerts à ceux qui ont candidaté pour cette formation:
                <ul class='option'>
                    <li> <i class="fa-solid fa-code fa-spin-pulse"></i><strong>SLAM (Solutions Logiciels Applications Métiers )</strong> :</li>
                    <p> Cette Option prépare des informaticiens spécialisés dans <strong><i>la conception et la maintenance de programmes applicatifs</i> </strong> aux services des entreprises ou d'acteurs divers.<br>
                        Ces derniers pourront à terme de leur formation, exercer en tant que développeur et/ou intégrateur web, analyste d'application,  gestionnaire de bases de données , technicien/administrateur du parc informatique, professeur en informatique ou architecte d'un réseau d'une structure. 
                 </p>
                 <li> <i class="fa-solid fa-tower-cell fa-spin-pulse"></i><strong> SISR (Solutions Informatiques Systèmes Réseaux )</strong> : </li>
                <p> L'option SISR s'adresse aux étudiants projetant de s'orienter vers <strong><i>la conception et la maintenance de l'infrastruscture informatique </i></strong> des entreprises. <br>
                        Les détenteurs d'un BTS SIO option SISR peuvent exercer en tant que administrateur réseau, technicien support, technicien d'exploitation ou technicien de maintenance. 
                    </p>
               
                </ul>
